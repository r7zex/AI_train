export function hasLatinLetters(value: string) {
  return /[A-Za-z]/.test(value)
}

export function getCourseGlossaryEntry(term: string): CourseGlossaryEntry | null {
  const normalized = term.trim().toLocaleLowerCase('en-US')
  const direct = dictionary.get(normalized)
  if (direct) return direct

  const embeddedKey = [...dictionary.keys()]
    .filter((key) => key.length >= 2 && normalized.includes(key))
    .sort((left, right) => right.length - left.length)[0]
  return embeddedKey ? dictionary.get(embeddedKey) ?? null : null
}

export function getCourseGlossaryEntries(terms: string[]) {
  return terms
    .filter(hasLatinLetters)
    .map((term) => getCourseGlossaryEntry(term))
    .filter((entry): entry is CourseGlossaryEntry => entry !== null)
}

