const theoryStep = (
  id: string,
  title: string,
  summary: string,
  sections: FlowStep['sections'],
  codeExample?: FlowStep['codeExample'],
): FlowStep => ({
  id,
  type: 'theory',
  title,
  summary,
  sections,
  ...(codeExample ? { codeExample } : {}),
})

const quizStep = (id: string, title: string, summary: string, quiz: Quiz): FlowStep => ({
  id,
  type: 'quiz',
  title,
  summary,
  quiz,
})

const practiceStep = (id: string, title: string, summary: string, task: PracticeTask): FlowStep => ({
  id,
  type: 'practice',
  title,
  summary,
  practiceTasks: [task],
})

const singleQuiz = (
  id: string,
  title: string,
  topicId: string,
  sectionId: string,
  question: string,
  options: Array<{ id: string; text: string }>,
  correctAnswer: string,
  explanation: string,
): Quiz => ({
  id,
  title,
  description: title,
  topicId,
  sectionId,
  questions: [
    {
      id: `${id}-q1`,
      topicId,
      sectionId,
      type: 'single',
      question,
      options,
      correctAnswer,
      explanation,
      difficulty: 'easy',
    },
  ],
})

const makeStdinTask = (
  id: string,
  title: string,
  statement: string,
  starterCode: string,
  sampleTests: PracticeTask['sampleTests'],
  hiddenTests: PracticeTask['hiddenTests'],
  tips: string[] = ['Считайте ввод через input().', 'Выведите ответ через print().', 'Проверьте крайние случаи.'],
): PracticeTask => ({
  id,
  title,
  kind: 'stdin-stdout',
  language: 'python',
  statement,
  tips,
  starterCode,
  sampleTests,
  hiddenTests,
})

function sec(id: string, title: string, what: string, why: string, where: string, example: string): FlowStep['sections'] {
  return [
    {
      id,
      title,
      paragraphs: [
        `Что это: ${what}`,
        `Зачем нужно: ${why}`,
        `Где в ML/ИИ: ${where}`,
        `Короткий пример: ${example}`,
      ],
    },
  ]
}

