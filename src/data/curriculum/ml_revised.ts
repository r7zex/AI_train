const legacyTopics = [
  ...mlFoundationsTopics,
  ...mlAdvancedFoundationsTopics,
  ...mlMasteryTopics,
]

function legacyTopic(id: string) {
  const topic = legacyTopics.find((item) => item.id === id)
  if (!topic) throw new Error(`Не найдена прежняя тема блока 4: ${id}`)
  return topic
}

function preservedChecks(ids: string[]) {
  return ids.flatMap((id) => legacyTopic(id).steps.filter((step) => step.type === 'quiz' || step.type === 'practice'))
}

function uniqueSources(ids: string[]) {
  const sourceMap = new Map<string, SourceLink>()
  for (const topic of ids.map(legacyTopic)) {
    for (const source of topic.sources) sourceMap.set(source.url, source)
    for (const step of topic.steps) {
      for (const source of step.sources ?? []) sourceMap.set(source.url, source)
    }
  }
  return [...sourceMap.values()]
}

function countChecks(steps: FlowStep[]) {
  return {
    quizQuestions: steps.reduce((total, step) => total + (step.quiz?.questions.length ?? 0), 0),
    practiceTasks: steps.reduce((total, step) => total + (step.practiceTasks?.length ?? 0), 0),
    examples: steps.reduce((total, step) => total
      + (step.workedExample?.length ?? 0)
      + (step.codeExample ? 1 : 0)
      + (step.sections?.reduce((sectionTotal, item) => sectionTotal + (item.codeExamples?.length ?? 0), 0) ?? 0), 0),
  }
}

function topic(options: {
  id: string
  title: string
  order: number
  summary: string
  explanation: string
  terminology: string[]
  formulas: string[]
  cheatsheet: string[]
  theory: FlowStep[]
  legacyIds: string[]
  estimatedMinutes: number
  examples: number
}): FlowTopic {
  const steps = [...options.theory, ...preservedChecks(options.legacyIds)]
  const checks = countChecks(steps)
  const result = mlFoundationsTopic(
    options.id,
    options.title,
    options.order,
    options.summary,
    options.explanation,
    options.terminology,
    options.formulas,
    options.cheatsheet,
    steps,
  )

  return {
    ...result,
    sources: uniqueSources(options.legacyIds),
    learningDesign: {
      format: 'теория → пример → код → проверка → практика',
      estimatedMinutes: options.estimatedMinutes,
      quizQuestions: checks.quizQuestions,
      practiceTasks: checks.practiceTasks,
      examples: checks.examples,
    },
  }
}

